#Tom liste
hoydeAar = []

#Append blir brukt for å legge til, den første blir da verdi 0, og den andre blir verdi 1
hoydeAar.append(50)
hoydeAar.append(76)

#En måte å printe ut på, men kaller ikke på variabelen
print([50,76] + [87,96])

#Legger til flere verdier til variabelen
hoydeAar = hoydeAar + [87,96]

#Printer ut variabelen
print(hoydeAar)
